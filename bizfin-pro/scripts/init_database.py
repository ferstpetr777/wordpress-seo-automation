        template_count = cursor.fetchone()[0]
        
        logger.info(f"‚úÖ –ù–∞—á–∞–ª—å–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ: {company_count} –∫–æ–º–ø–∞–Ω–∏–π, {template_count} —à–∞–±–ª–æ–Ω–æ–≤")
        
