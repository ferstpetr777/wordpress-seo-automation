        # –ß–∏—Ç–∞–µ–º —Å—Ö–µ–º—É –∏–∑ —Ñ–∞–π–ª–∞
        schema_file = os.path.join(os.path.dirname(__file__), '..', 'db', 'schema.sql')
        
        if not os.path.exists(schema_file):
            logger.error(f"‚ùå –§–∞–π–ª —Å—Ö–µ–º—ã –Ω–µ –Ω–∞–π–¥–µ–Ω: {schema_file}")
            return False
        
